#include "libft.h"

void	*ft_memset(void *ptr, int c, size_t n)
{
	unsigned char	*p;

	p = (unsigned char *) ptr;
	while (n > 0)
	{
		*p = c;
		n--;
		p++;
	}
	return (ptr);
}
